package com.omfysgroup.ajanhcm.model;

public class CertificationPercentageCal {

	private double open;
	
	private double in_progressCer;
	
	private double completed_cer;
	
	private double on_hold;
	
	private double exam_failed;
	
	private double withdrawal;

	public double getOpen() {
		return open;
	}

	public void setOpen(double open) {
		this.open = open;
	}

	public double getIn_progressCer() {
		return in_progressCer;
	}

	public void setIn_progressCer(double in_progressCer) {
		this.in_progressCer = in_progressCer;
	}

	public double getCompleted_cer() {
		return completed_cer;
	}

	public void setCompleted_cer(double completed_cer) {
		this.completed_cer = completed_cer;
	}

	public double getOn_hold() {
		return on_hold;
	}

	public void setOn_hold(double on_hold) {
		this.on_hold = on_hold;
	}

	public double getExam_failed() {
		return exam_failed;
	}

	public void setExam_failed(double exam_failed) {
		this.exam_failed = exam_failed;
	}

	public double getWithdrawal() {
		return withdrawal;
	}

	public void setWithdrawal(double withdrawal) {
		this.withdrawal = withdrawal;
	}
	
}
