package com.omfysgroup.ajanhcm.model;

public class CertificationCountDashboard {
	
	private int assigned;
	
	private int open;
	
	private int in_progressCer;
	
	private int completed_cer;
	
	private int on_hold;
	
	private int exam_failed;
	
	private int withdrawal;

	public int getAssigned() {
		return assigned;
	}

	public void setAssigned(int assigned) {
		this.assigned = assigned;
	}

	public int getOpen() {
		return open;
	}

	public void setOpen(int open) {
		this.open = open;
	}

	public int getIn_progressCer() {
		return in_progressCer;
	}

	public void setIn_progressCer(int in_progressCer) {
		this.in_progressCer = in_progressCer;
	}

	public int getCompleted_cer() {
		return completed_cer;
	}

	public void setCompleted_cer(int completed_cer) {
		this.completed_cer = completed_cer;
	}

	public int getOn_hold() {
		return on_hold;
	}

	public void setOn_hold(int on_hold) {
		this.on_hold = on_hold;
	}

	public int getExam_failed() {
		return exam_failed;
	}

	public void setExam_failed(int exam_failed) {
		this.exam_failed = exam_failed;
	}

	public int getWithdrawal() {
		return withdrawal;
	}

	public void setWithdrawal(int withdrawal) {
		this.withdrawal = withdrawal;
	}


}
